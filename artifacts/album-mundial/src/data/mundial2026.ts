export type Position = "PO" | "DEF" | "MED" | "DEL";

export interface Player {
  name: string;
  position: Position;
}

export interface Country {
  id: string;
  name: string;
  flag: string;
  players: Player[];
}

export interface Group {
  id: string;
  name: string;
  countries: Country[];
}

export interface Sticker {
  id: string;
  number: number;
  label: string;
  type: "flag" | "player" | "team";
  position?: Position;
}

export function getCountryStickers(country: Country): Sticker[] {
  const stickers: Sticker[] = [];
  // #1 — Bandera
  stickers.push({ id: `${country.id}-1`, number: 1, label: "Bandera", type: "flag" });
  // #2–12 — primeros 11 jugadores
  country.players.slice(0, 11).forEach((p, i) => {
    stickers.push({
      id: `${country.id}-${i + 2}`,
      number: i + 2,
      label: p.name,
      type: "player",
      position: p.position,
    });
  });
  // #13 — Foto grupal
  stickers.push({ id: `${country.id}-13`, number: 13, label: "Foto Grupal", type: "team" });
  // #14–20 — jugadores 12–18
  country.players.slice(11, 18).forEach((p, i) => {
    stickers.push({
      id: `${country.id}-${i + 14}`,
      number: i + 14,
      label: p.name,
      type: "player",
      position: p.position,
    });
  });
  return stickers;
}

