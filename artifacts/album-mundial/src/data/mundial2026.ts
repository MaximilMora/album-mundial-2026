export type Position = "PO" | "DEF" | "MED" | "DEL";

export interface Player {
  name: string;
  position: Position;
}

export interface Country {
  id: string;
  name: string;
  flag: string;
  players: Player[];
}

export interface Group {
  id: string;
  name: string;
  countries: Country[];
}

export interface Sticker {
  id: string;
  number: number;
  label: string;
  type: "flag" | "player" | "team";
  position?: Position;
}

export function getCountryStickers(country: Country): Sticker[] {
  const stickers: Sticker[] = [];
  stickers.push({ id: `${country.id}-1`, number: 1, label: "Bandera", type: "flag" });
  country.players.slice(0, 11).forEach((p, i) => {
    stickers.push({ id: `${country.id}-${i + 2}`, number: i + 2, label: p.name, type: "player", position: p.position });
  });
  stickers.push({ id: `${country.id}-13`, number: 13, label: "Equipo", type: "team" });
  country.players.slice(11, 18).forEach((p, i) => {
    stickers.push({ id: `${country.id}-${i + 14}`, number: i + 14, label: p.name, type: "player", position: p.position });
  });
  return stickers;
}

